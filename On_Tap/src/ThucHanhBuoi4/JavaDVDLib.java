    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        try {

            String sql = "INSERT INTO DVD (DVDCodeNo, DVDTitle, DVDLang, DVDSubTitles, DVDPrice) VALUES (?,?,?,?,?)";
            PreparedStatement psmt = con.prepareStatement(sql);

            psmt.setInt(1, Integer.parseInt(txt_codeNumber.getText()));
            psmt.setString(2, txt_title.getText());
            psmt.setString(3, cbb_language.getSelectedItem().toString());

            boolean bl;
            if (rdb_yes.isSelected()) {
                bl = true;
            } else {
                bl = false;
            }
            psmt.setBoolean(4, bl);
            psmt.setFloat(5, Float.parseFloat(spiner_price.getValue().toString()));

            JOptionPane.showMessageDialog(this, sql);
            psmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Đã thêm thành công!");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            Statement cmd = con.createStatement();
            ResultSet rs = cmd.executeQuery("SELECT DVDCodeNo FROM DVD");
            cbb_codeNumber.removeAllItems();
            while (rs.next()) {
                int item = rs.getInt(1);
                cbb_codeNumber.addItem(String.valueOf(item));
            }
            btn_save.setEnabled(true);
            btn_addNew.setEnabled(false);
            btn_delete.setEnabled(false);
            btn_cancel.setEnabled(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void cbb_codeNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbb_codeNumberActionPerformed
        // TODO add your handling code here:
        try {
            Statement cmd = con.createStatement();
            ResultSet rs = cmd.executeQuery("SELECT * FROM DVD WHERE DVDCodeNo=" + cbb_codeNumber.getSelectedItem());
            if (rs.next()) {
                txt_codeNumber.setText(rs.getInt(1) + "");
                txt_title.setText(rs.getString(2));
                cbb_language.setSelectedItem(rs.getString(3));

                rdb_yes.setSelected(rs.getBoolean(4));

                spiner_price.setValue(rs.getBigDecimal(5));
            }
            btn_save.setEnabled(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_cbb_codeNumberActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try {
            Statement cmd = con.createStatement();
            int rs = cmd.executeUpdate("DELETE FROM DVD WHERE DVDCodeNo=" + cbb_codeNumber.getSelectedItem());
            JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
            cbb_codeNumber.removeItem(cbb_codeNumber.getSelectedItem());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

